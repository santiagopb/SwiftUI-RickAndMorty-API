import SwiftUI

extension Color {
    
    internal struct UI {
        let Black = Color("Black")
        let Gray = Color("Gray")
        let Green = Color("Green")
        let Orange = Color("Orange")
        let Primary = Color("Primary")
        let Red = Color("Red")
        let White = Color("White")
        let WhiteBroken = Color("WhiteBroken")
        let WhiteSmock = Color("WhiteSmoke")
    }
    
    static let ui = UI()
}
